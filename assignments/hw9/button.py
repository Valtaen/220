from graphics import *

class Button():

    def __init__(self, shape, label):
        self.shape = shape
        self.label = label

    def get_label(self):
        return self.label

    def draw(self, win):
        self.draw(win)

    def undraw(self, win):
        self.undraw(win)

    #def is_clicked(self, point):

    def color_button(self, color):
        self.setFill = color

    def set_label(self, label):
        self.label = label
